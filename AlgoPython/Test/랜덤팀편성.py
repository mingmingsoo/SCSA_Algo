import random

members = ["예랑", "예원", "승기", "온유", "준영", "혜준", "서현", "혜민", "승원", "나연", "정우"]

weeks = {
    "2월 2주차 (2025-02-10~02-16)": [],
    "2월 3주차 (2025-02-17~02-23)": [],
    "2월 4주차 (2025-02-24~03-02)": [],
    "3월 1주차 (2025-03-03~03-09)": [],
    "3월 2주차 (2025-03-10~03-16)": [],
    "3월 3주차 (2025-03-17~03-23)": [],
    "3월 4주차 (2025-03-24~03-30)": [],
    "3월 5주차 (2025-03-31~04-06)": [],
}

for week in weeks:
    random.shuffle(members)

    team1 = members[:len(members) // 2]
    team2 = members[len(members) // 2:]

    print(f"📅 {week} 팀 배정")
    print("6층:", " ".join(team1))  # 따옴표 제거
    print("5층:", " ".join(team2))  # 따옴표 제거
    print("-" * 30)
